package in.co.rays.javabasic;

public class Datatype {
	public static void main(String[] args) {
		Boolean a = true;
		
		System.out.println("Int =" + 19);
		System.out.println("Float =" + 18000F);
		System.out.println("Double =" + 15.23);
		System.out.println("Long =" + 199999999999L);
		System.out.println("String =" + "ABCD$%##");
		System.out.println("Boolean =" + a);
		
	}

}
